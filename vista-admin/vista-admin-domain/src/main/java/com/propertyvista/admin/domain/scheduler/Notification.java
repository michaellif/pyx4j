package com.propertyvista.admin.domain.scheduler;

import com.pyx4j.entity.annotations.Detached;
import com.pyx4j.entity.annotations.JoinColumn;
import com.pyx4j.entity.annotations.MemberColumn;
import com.pyx4j.entity.annotations.OrderColumn;
import com.pyx4j.entity.annotations.Owner;
import com.pyx4j.entity.annotations.ReadOnly;
import com.pyx4j.entity.annotations.ToString;
import com.pyx4j.entity.annotations.ToStringFormat;
import com.pyx4j.entity.annotations.validator.NotNull;
import com.pyx4j.entity.shared.IEntity;
import com.pyx4j.entity.shared.IPrimitive;

import com.propertyvista.domain.security.AdminUser;

@ToStringFormat("{0} {1}")
public interface Notification extends IEntity {

    @Owner
    @JoinColumn
    @ReadOnly
    @Detached
    @MemberColumn(name = "trgr")
    Trigger trigger();

    @OrderColumn
    IPrimitive<Integer> odr();

    @ToString
    @NotNull
    IPrimitive<NotificationEvent> event();

    @ToString
    @NotNull
    @MemberColumn(name = "usr")
    AdminUser user();

}
