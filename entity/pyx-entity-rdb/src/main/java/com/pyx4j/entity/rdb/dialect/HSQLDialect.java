package com.pyx4j.entity.rdb.dialect;

import java.math.BigDecimal;
import java.sql.SQLException;

import com.pyx4j.entity.rdb.cfg.Configuration.DatabaseType;
import com.pyx4j.entity.rdb.cfg.Configuration.MultitenancyType;

public class HSQLDialect extends Dialect {

    private final boolean sequencesBaseIdentity;

    public HSQLDialect(NamingConvention namingConvention, MultitenancyType multitenancyType, boolean sequencesBaseIdentity) {
        super(DatabaseType.HSQLDB, namingConvention, multitenancyType);
        addTypeMeta(Short.class, "smallint");
        addTypeMeta(Byte.class, "tinyint");
        addTypeMeta(Long.class, "bigint");
        addTypeMeta(Double.class, "double");
        addTypeMeta(Boolean.class, "boolean");
        addTypeMeta(byte[].class, "longvarbinary", "varbinary");
        addTypeMeta(java.util.Date.class, "timestamp");

        // TODO use annotation for scale
        addTypeMeta(BigDecimal.class, "decimal", 18, 2);
        this.sequencesBaseIdentity = sequencesBaseIdentity;
    }

    @Override
    public int identifierMaximumLength() {
        return 128;
    }

    @Override
    public String getGeneratedIdColumnString() {
        if (sequencesBaseIdentity) {
            return "NOT NULL";
        } else {
            return "generated by default as identity (start with 1)";
        }
    }

    @Override
    public boolean isSequencesBaseIdentity() {
        return sequencesBaseIdentity;
    }

    @Override
    public String getSequenceNextValSql(String sequenceName) {
        if (sequencesBaseIdentity) {
            return "NEXT VALUE FOR " + sequenceName;
        } else {
            return super.getSequenceNextValSql(sequenceName);
        }
    }

    @Override
    public String sqlSequenceMetaData() {
        StringBuilder sql = new StringBuilder();
        sql.append("SELECT sequence_name FROM INFORMATION_SCHEMA.SEQUENCES");
        return sql.toString();
    }

    @Override
    public String getCreateSequenceSql(String sequenceName, int itentityOffset) {
        if (sequencesBaseIdentity) {
            return "CREATE SEQUENCE " + sequenceName + ((itentityOffset != 0) ? (" START WITH " + itentityOffset) : "");
        } else {
            return super.getCreateSequenceSql(sequenceName, itentityOffset);
        }
    }

    @Override
    public String getDropSequenceSql(String sequenceName) {
        if (sequencesBaseIdentity) {
            return "drop sequence " + sequenceName;
        } else {
            return super.getDropSequenceSql(sequenceName);
        }
    }

    @Override
    public String sqlAlterIdentityColumn(String tableName, int itentityOffset) {
        if (sequencesBaseIdentity) {
            return null;
        } else {
            return "ALTER TABLE  " + tableName + "  ALTER COLUMN " + this.getNamingConvention().sqlIdColumnName()
                    + " bigint generated by default as identity (start with " + itentityOffset + ")";
        }
    }

    @Override
    public String sqlDropForeignKey(String tableName, String constraintName) {
        return "ALTER TABLE " + tableName + " DROP CONSTRAINT " + constraintName;
    }

    @Override
    public String applyLimitCriteria(String sql) {
        return sql + " LIMIT ? OFFSET ?";
    }

    @Override
    public boolean limitCriteriaIsRelative() {
        return true;
    }

    @Override
    public boolean isUniqueConstraintException(SQLException e) {
        return false;
    }
}
