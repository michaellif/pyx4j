package com.pyx4j.entity.rdb.mapping;

import com.pyx4j.entity.core.IEntity;
import com.pyx4j.entity.core.meta.MemberMeta;

public class MemberCollectionOperationsMeta extends MemberExternalOperationsMeta {

    private final boolean autogenerated;

    private final MemberCollectionOrderMeta collectionOrderMeta;

    public MemberCollectionOperationsMeta(EntityMemberAccess memberAccess, ValueAdapter valueAdapter, String sqlName, MemberMeta memberMeta, String memberPath,
            Class<? extends IEntity> joinTableClass, boolean joinTableSameAsTarget, String sqlOwnerName, ValueAdapter ownerValueAdapter, String sqlValueName,
            MemberCollectionOrderMeta collectionOrderMeta, String sqlChildJoinContition, boolean autogenerated) {
        super(memberAccess, valueAdapter, sqlName, memberMeta, memberPath, joinTableClass, joinTableSameAsTarget, sqlOwnerName, ownerValueAdapter, sqlValueName,
                sqlChildJoinContition);
        this.collectionOrderMeta = collectionOrderMeta;
        this.autogenerated = autogenerated;
    }

    public String sqlOrderColumnName() {
        return collectionOrderMeta.sqlOrderColumnName();
    }

    public boolean isOrderMaterialized() {
        return (collectionOrderMeta != null) && collectionOrderMeta.isOrderMaterialized();
    }

    public String orderMemberName() {
        return collectionOrderMeta.orderMemberName();
    }

    public boolean isAutogenerated() {
        return autogenerated;
    }

    @Override
    public String toString() {
        return super.toString() + " Collection";
    }
}
