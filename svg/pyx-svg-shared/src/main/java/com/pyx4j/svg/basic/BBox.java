package com.pyx4j.svg.basic;

public class BBox {
    private int x;

    private int y;

    private int width;

    private int height;

    public BBox() {
        x = 0;
        y = 0;
        width = 0;
        height = 0;
    }

    public BBox(int width, int height) {
        if (width >= 0)
            this.width = width;
        if (height >= 0)
            this.height = height;
    }

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }

    public int getWidth() {
        return width;
    }

    public void setWidth(int width) {
        this.width = width;
    }

    public int getHeight() {
        return height;
    }

    public void setHeight(int height) {
        this.height = height;
    }

}
